// Sudoku puzzle schema and types

// A Sudoku board is a 9x9 grid of numbers (0-9, where 0 represents empty)
// board: array of 9 arrays, each containing 9 numbers

export const createEmptyBoard = () => {
  return Array(9).fill(null).map(() => Array(9).fill(0));
};

export const isValidBoard = (board) => {
  if (!Array.isArray(board) || board.length !== 9) return false;
  
  for (let row of board) {
    if (!Array.isArray(row) || row.length !== 9) return false;
    for (let cell of row) {
      if (typeof cell !== 'number' || cell < 0 || cell > 9) return false;
    }
  }
  
  return true;
};

